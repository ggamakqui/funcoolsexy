DROP TABLE C_NOTICE;
CREATE TABLE C_NOTICE(
    NNO NUMBER PRIMARY KEY,
    NTITLE VARCHAR2(100) NOT NULL,
    NCONTENT VARCHAR2(1000),
    MID VARCHAR2(20),
    NREGDATE DATE,
    NHIT NUMBER,
    CONSTRAINT NOTICE_MEMBER_FK FOREIGN KEY (MID) REFERENCES C_MEMBER (MID) 
);

DROP SEQUENCE C_NOTICE_SEQ;
CREATE SEQUENCE C_NOTICE_SEQ
START WITH 1
INCREMENT BY 1
MAXVALUE 999999
NOCACHE
NOCYCLE;