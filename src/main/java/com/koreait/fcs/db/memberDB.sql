DROP TABLE C_MEMBER;
CREATE TABLE C_MEMBER(
    MNO NUMBER PRIMARY KEY,
    MID VARCHAR2(20) UNIQUE NOT NULL,
    MNAME VARCHAR2(100),
    MPW VARCHAR2(50) NOT NULL,
    MMOBILE1 CHAR(3),
    MMOBILE2 CHAR(4),
    MMOBILE3 CHAR(4),
    MEMAIL VARCHAR2(50) UNIQUE NOT NULL,
    MBIRTH CHAR(6),
    MPOST VARCHAR2(20),
    MADDR1 VARCHAR2(100),
    MADDR2 VARCHAR2(100),
    MADDR3 VARCHAR2(100),
    MVALIDATE CHAR(1),
    MREGDATE DATE
);


DROP SEQUENCE C_MEMBER_SEQ;
CREATE SEQUENCE C_MEMBER_SEQ
START WITH 1
INCREMENT BY 1
MAXVALUE 999999
NOCACHE
NOCYCLE;

